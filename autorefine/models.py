class CompletionResponse(BaseModel):
    """Wrapper returned to the caller after a chat/complete call.

    Contains the model output alongside billing metadata and the prompt
    version that was active at call time.
    """

    id: str = Field(
        default_factory=_new_id,
        description="The interaction ID (matches the stored Interaction).",
    )
    text: str = Field(
        default="",
        description="The model's text response.",
    )
    messages: list[Message] = Field(
        default_factory=list,
        description="Full message list including the assistant's reply.",
    )
    model: str = Field(
        default="",
        description="Model identifier that produced this response.",
    )
    input_tokens: int = Field(
        default=0,
        ge=0,
        description="Input tokens consumed.",
    )
    output_tokens: int = Field(
        default=0,
        ge=0,
        description="Output tokens generated.",
    )
    cost_usd: float = Field(
        default=0.0,
        ge=0.0,
        description="Estimated cost in US dollars.",
    )
    prompt_version: int = Field(
        default=0,
        ge=0,
        description="Version of the system prompt that was active for this call.",
    )
    metadata: dict[str, Any] = Field(
        default_factory=dict,
        description="Arbitrary key-value pairs for caller-specific context.",
    )


# ---------------------------------------------------------------------------
# A/B test state
# ---------------------------------------------------------------------------

class ABTest(BaseModel):
    """Tracks the state of an A/B test between two prompt versions.

    Traffic is split between ``control_version`` (the current active prompt)
    and ``candidate_version`` (the proposed improvement).  Once both variants
    accumulate enough interactions the test auto-resolves.
    """

    id: str = Field(
        default_factory=_new_id,
        description="Unique A/B test identifier.",
    )
    prompt_key: str = Field(
        default="default",
        description="Prompt namespace under test.",
    )
    control_version: int = Field(
        ge=0,
        description="Version number of the current active (control) prompt.",
    )
    candidate_version: int = Field(
        ge=0,
        description="Version number of the candidate prompt being tested.",
    )
    split_ratio: float = Field(
        default=0.2,
        ge=0.0,
        le=1.0,
        description="Fraction of traffic routed to the candidate (0.0–1.0).",
    )
    control_interactions: int = Field(
        default=0,
        ge=0,
        description="Number of interactions served by the control variant.",
    )
    candidate_interactions: int = Field(
        default=0,
        ge=0,
        description="Number of interactions served by the candidate variant.",
    )
    control_score: float = Field(
        default=0.0,
        description="Rolling average feedback score for the control variant.",
    )
    candidate_score: float = Field(
        default=0.0,
        description="Rolling average feedback score for the candidate variant.",
    )
    is_active: bool = Field(
        default=True,
        description="Whether this test is currently running.",
    )
    created_at: datetime = Field(
        default_factory=_utc_now,
        description="UTC timestamp when the test started.",
    )
    completed_at: datetime | None = Field(
        default=None,
        description="UTC timestamp when the test finished (None if still running).",
    )
    result: str = Field(
        default="",
        description="Outcome: 'promoted', 'rejected', 'superseded', or '' (still running).",
    )

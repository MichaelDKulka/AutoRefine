@main.command()
def stats():
    """Print a summary table of feedback, scores, and costs."""
    store, _ = _get_store()

    keys = set()
    for k in ("default", "support-billing", "support-technical", "content-writer"):
        if store.get_prompt_history(k):
            keys.add(k)
    if not keys:
        keys.add("default")

    if _HAS_RICH:
        table = Table(title="AutoRefine Stats (last 30 days)")
        table.add_column("Prompt Key", style="cyan")
        table.add_column("Interactions", justify="right")
        table.add_column("Feedback", justify="right")
        table.add_column("Avg Score", justify="right")
        table.add_column("Versions", justify="right")
        table.add_column("Active", style="green", justify="right")
        for k in sorted(keys):
            a = store.get_analytics(k, days=30)
            table.add_row(
                k,
                str(a["total_interactions"]),
                str(a["total_feedback"]),
                f"{a['average_score']:+.2f}",
                str(a["total_versions"]),
                f"v{a['active_version']}",
            )
        _console.print(table)
        cost = store.get_monthly_refiner_cost()
        _console.print(f"\n  Monthly refiner cost: [bold]${cost:.4f}[/bold]")
    else:
        click.echo(f"{'Key':30s} {'Ix':>6s} {'FB':>6s} {'Score':>7s} {'Ver':>4s} {'Active':>7s}")
        click.echo("-" * 70)
        for k in sorted(keys):
            a = store.get_analytics(k, days=30)
            click.echo(
                f"{k:30s} {a['total_interactions']:6d} {a['total_feedback']:6d} "
                f"{a['average_score']:+7.2f} {a['total_versions']:4d} v{a['active_version']:>5d}"
            )
        click.echo(f"\nMonthly refiner cost: ${store.get_monthly_refiner_cost():.4f}")



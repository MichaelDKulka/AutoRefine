@main.command()
@click.argument("key", default="default")
@click.option("--confirm", is_flag=True, help="Skip confirmation prompt.")
def reset(key: str, confirm: bool):
    """Wipe all data for a prompt key."""
    store, _ = _get_store()

    history = store.get_prompt_history(key)
    feedback = store.get_feedback(prompt_key=key, limit=100_000)
    interactions = store.get_interactions(prompt_key=key, limit=100_000)

    _print(f"This will delete for key '{key}':")
    _print(f"  {len(history)} prompt versions")
    _print(f"  {len(interactions)} interactions")
    _print(f"  {len(feedback)} feedback signals")

    if not confirm and not click.confirm("Are you sure? This cannot be undone"):
        _print("Cancelled.")
        return

    # Purge by setting a far-future cutoff (purges everything)
    # Then deactivate all versions
    count = store.purge_old_data(datetime.now(timezone.utc) + timedelta(days=1))
    store.set_active_version(key, -1)
    _print(f"Deleted {count} records.")



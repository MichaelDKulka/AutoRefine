@main.command()
@click.argument("key", default="default")
@click.option("--format", "fmt", type=click.Choice(["json", "md"]), default="json", help="Output format.")
def export(key: str, fmt: str):
    """Export the full prompt evolution history."""
    store, _ = _get_store()
    history = store.get_prompt_history(key)
    if not history:
        _print(f"No history for key '{key}'")
        return

    if fmt == "json":
        data = [
            {
                "version": pv.version,
                "system_prompt": pv.system_prompt,
                "parent_version": pv.parent_version,
                "changelog": pv.changelog,
                "is_active": pv.is_active,
                "created_at": pv.created_at.isoformat(),
                "performance_score": pv.performance_score,
            }
            for pv in history
        ]
        click.echo(json.dumps({"prompt_key": key, "versions": data}, indent=2))

    elif fmt == "md":
        click.echo(f"# Prompt History: `{key}`\n")
        for pv in history:
            active = " **(active)**" if pv.is_active else ""
            click.echo(f"## Version {pv.version}{active}\n")
            click.echo(f"- Created: {pv.created_at.isoformat()}")
            if pv.changelog:
                click.echo(f"- Changelog: {pv.changelog}")
            if pv.parent_version is not None:
                click.echo(f"- Parent: v{pv.parent_version}")
            click.echo(f"\n```\n{pv.system_prompt}\n```\n")



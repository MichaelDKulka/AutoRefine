@main.command()
def init():
    """Interactive setup -- creates .env and a starter script."""
    if _HAS_RICH:
        _console.print(f"[bold blue]{_BANNER}[/bold blue]")
    else:
        click.echo(_BANNER)

    mode = click.prompt(
        "How do you want to run AutoRefine?\n"
        "  1. Self-hosted (provide your own LLM API keys)\n"
        "  2. AutoRefine Cloud (get an API key from autorefine.dev)\n"
        "Choose",
        type=click.Choice(["1", "2"]), default="1",
    )

    if mode == "2":
        _print("\nVisit https://autorefine.dev/signup to get your API key.")
        api_key = click.prompt("Paste your AutoRefine API key (ar_live_...)", default="", show_default=False)
        model = click.prompt("Model", default="gpt-4o")
        env_path = Path(".env")
        lines = [
            f"AUTOREFINE_API_KEY={api_key}",
            f"AUTOREFINE_MODEL={model}",
            "AUTOREFINE_AUTO_LEARN=true",
        ]
        env_path.write_text("\n".join(lines) + "\n", encoding="utf-8")
        _print(f"\n  Wrote {env_path.resolve()}")
        _print("  No refiner key needed -- AutoRefine Cloud handles refinement.")

        starter = Path("quickstart.py")
        if not starter.exists():
            starter.write_text(
                '"""AutoRefine Cloud quickstart -- generated by `autorefine init`."""\n\n'
                'from autorefine import AutoRefine\n\n'
                'client = AutoRefine()  # reads .env automatically -- routes through cloud\n'
                'client.set_system_prompt("You are a helpful assistant.")\n\n'
                'resp = client.complete("You are a helpful assistant.", "Hello!")\n'
                'print(resp.text)\n\n'
                '# client.feedback(resp.id, "thumbs_up")\n',
                encoding="utf-8",
            )
            _print(f"  Wrote {starter.resolve()}")
        _print("\n  Run `python quickstart.py` to get started!")
        return

    api_key = click.prompt("Primary LLM API key (e.g. sk-...)", default="", show_default=False)
    model = click.prompt("Model", default="gpt-4o")
    refiner_key = click.prompt("Refiner API key (Anthropic, for auto-refinement)", default="", show_default=False)
    storage = click.prompt("Storage backend", type=click.Choice(["json", "sqlite", "postgres"]), default="json")

    db_url = ""
    if storage == "postgres":
        db_url = click.prompt("PostgreSQL connection URL")

    # Write .env
    env_path = Path(".env")
    lines = [
        f"AUTOREFINE_API_KEY={api_key}",
        f"AUTOREFINE_MODEL={model}",
        f"AUTOREFINE_STORAGE_BACKEND={storage}",
    ]
    if refiner_key:
        lines.append(f"AUTOREFINE_REFINER_KEY={refiner_key}")
        lines.append("AUTOREFINE_AUTO_LEARN=true")
    if db_url:
        lines.append(f"AUTOREFINE_DATABASE_URL={db_url}")

    env_path.write_text("\n".join(lines) + "\n", encoding="utf-8")
    _print(f"\n  Wrote {env_path.resolve()}")

    # Write starter script
    starter = Path("quickstart.py")
    if not starter.exists():
        starter.write_text(
            '"""AutoRefine quickstart — generated by `autorefine init`."""\n\n'
            'from autorefine import AutoRefine\n\n'
            'client = AutoRefine()  # reads .env automatically\n'
            'client.set_system_prompt("You are a helpful assistant.")\n\n'
            'resp = client.complete("You are a helpful assistant.", "Hello!")\n'
            'print(resp.text)\n\n'
            '# client.feedback(resp.id, "thumbs_up")\n',
            encoding="utf-8",
        )
        _print(f"  Wrote {starter.resolve()}")

    _print("\n  Run [bold]python quickstart.py[/bold] to get started!" if _HAS_RICH
           else "\n  Run `python quickstart.py` to get started!")



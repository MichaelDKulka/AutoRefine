@prompts_group.command("list")
def prompts_list():
    """List all prompt keys and their active versions."""
    store, _ = _get_store()

    # Discover prompt keys by scanning all versions
    store.get_prompt_history("default")
    keys = {"default"}
    # Try common keys — the store doesn't have a "list all keys" method
    for k in ("default", "support-billing", "support-technical", "content-writer"):
        h = store.get_prompt_history(k)
        if h:
            keys.add(k)

    if _HAS_RICH:
        table = Table(title="Prompt Keys")
        table.add_column("Key", style="cyan")
        table.add_column("Active Version", style="green")
        table.add_column("Total Versions", style="yellow")
        for k in sorted(keys):
            active = store.get_active_prompt(k)
            history = store.get_prompt_history(k)
            if history:
                table.add_row(k, f"v{active.version}" if active else "-", str(len(history)))
        _console.print(table)
    else:
        for k in sorted(keys):
            active = store.get_active_prompt(k)
            history = store.get_prompt_history(k)
            if history:
                v = f"v{active.version}" if active else "-"
                click.echo(f"  {k:30s}  active={v}  versions={len(history)}")


@prompts_group.command("show")
@click.argument("key", default="default")
def prompts_show(key: str):
    """Print the current active prompt text."""
    store, _ = _get_store()
    active = store.get_active_prompt(key)
    if active is None:
        _print(f"No active prompt for key '{key}'")
        return

    if _HAS_RICH:
        _console.print(Panel(
            active.system_prompt,
            title=f"[cyan]{key}[/cyan] v{active.version}",
            subtitle=active.changelog or "",
            border_style="green",
        ))
    else:
        click.echo(f"--- {key} v{active.version} ---")
        click.echo(active.system_prompt)
        if active.changelog:
            click.echo(f"\nChangelog: {active.changelog}")


@prompts_group.command("diff")
@click.argument("key", default="default")
@click.argument("v1", type=int)
@click.argument("v2", type=int)
def prompts_diff(key: str, v1: int, v2: int):
    """Show a colored diff between two prompt versions."""
    store, _ = _get_store()
    pv1 = store.get_prompt_version(key, v1)
    pv2 = store.get_prompt_version(key, v2)
    if pv1 is None:
        _print(f"Version {v1} not found for key '{key}'")
        return
    if pv2 is None:
        _print(f"Version {v2} not found for key '{key}'")
        return

    diff_lines = list(difflib.unified_diff(
        pv1.system_prompt.splitlines(keepends=True),
        pv2.system_prompt.splitlines(keepends=True),
        fromfile=f"v{v1}", tofile=f"v{v2}",
    ))

    if not diff_lines:
        _print("No differences.")
        return

    if _HAS_RICH:
        _console.print(Syntax("".join(diff_lines), "diff", theme="monokai"))
    else:
        for line in diff_lines:
            if line.startswith("+"):
                click.secho(line, fg="green", nl=False)
            elif line.startswith("-"):
                click.secho(line, fg="red", nl=False)
            elif line.startswith("@@"):
                click.secho(line, fg="cyan", nl=False)
            else:
                click.echo(line, nl=False)


@prompts_group.command("rollback")
@click.argument("key", default="default")
@click.argument("version", type=int)
def prompts_rollback(key: str, version: int):
    """Roll back to a specific prompt version."""
    store, _ = _get_store()
    pv = store.get_prompt_version(key, version)
    if pv is None:
        _print(f"Version {version} not found for key '{key}'")
        return

    _print(f"Rolling back '{key}' to v{version}:")
    _print(f"  {pv.system_prompt[:120]}{'...' if len(pv.system_prompt) > 120 else ''}")

    if not click.confirm("Proceed?"):
        _print("Cancelled.")
        return

    store.set_active_version(key, version)
    _print(f"Rolled back to v{version}")



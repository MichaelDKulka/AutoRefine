@cloud_keys_group.command("list")
def cloud_keys_list():
    """List API keys for your organization."""
    import httpx
    _, cfg = _get_store()
    if not cfg.detect_cloud_mode():
        _print("Not in cloud mode. Set AUTOREFINE_API_KEY=ar_live_...")
        return
    try:
        resp = httpx.get(
            f"{cfg.cloud_base_url}/api/keys",
            headers={"Authorization": f"Bearer {cfg.api_key}"},
            timeout=10,
        )
        data = resp.json()
        if _HAS_RICH:
            table = Table(title="API Keys")
            table.add_column("Prefix", style="cyan")
            table.add_column("Name")
            table.add_column("Active", style="green")
            table.add_column("Created")
            for k in data.get("keys", []):
                table.add_row(k["key_prefix"], k.get("name", ""), str(k["is_active"]), k.get("created_at", "")[:10])
            _console.print(table)
        else:
            for k in data.get("keys", []):
                click.echo(f"  {k['key_prefix']}  name={k.get('name', '')}  active={k['is_active']}")
    except Exception as exc:
        _print(f"Error: {exc}")


